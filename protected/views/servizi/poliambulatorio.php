<h4>BCM – Impedenziometria corporea</h4>
<p>
    Valutazione della composizione corporea con studio delle ritenzioni idriche.
</p>

<h4>MOC – Mineralometria ossea</h4>
<p>
    Permette di valutare la condizione minerale dell&apos;osso per prevenire le 
    complicanze dell&apos;osteoporosi in particolare della donna in post menopausa.
</p>

<h4>Esame spirometrico</h4>
<p>
    Valuta la funzionalit&agrave; dei polmoni mediante test di funzionalit&agrave; respiratoria.
    Riconosce e tratta le patologie che alterano gli scambi respiratori.
</p>

<h4>Test stress ossidativo</h4>
<p>
    Tramite la misurazione dei radicali liberi si pu&ograve; monitorare lo stato di benessere psico-fisico della persona
    e il livello di stress ossidativo. Valuta il rischio cardiovascolare e consiglia differenti abitudini dietetiche 
    e una terapia antiossidante integrativa.</p>

